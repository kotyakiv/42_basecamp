int		ft_atoi(char *str)
{
	int total;
	int minus;

	minus = 1;
	total = 0;
	while (*str == ' ')
	{
		str++;
	}
	if (*str == '-')
	{
		minus = -1;
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		total *= 10;
		total += *str - '0';
		str++;
	}
	return (total * minus);
}
