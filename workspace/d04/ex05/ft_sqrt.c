int	ft_sqrt(int nb)
{
	int i;
	int no_sqr_root;

	no_sqr_root = 0;
	i = 1;
	while (i <= nb / i)
	{
		if (i * i == nb)
		{
			return (i);
		}
		i++;
	}
	return (no_sqr_root);
}
